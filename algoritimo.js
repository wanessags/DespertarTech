var numeroUm = 0;
let numeroDois = 10;
var numeroTres = 20;

//diferença entre const e var - as variáveis de const não podem ser atualizadas nem declaradas novamente, já var é uma variável que deve ser global, ou seja, o valor dessa variável poderá ser alterado a qualquer momento
//diferença entre let e var - let é uma váriavel não global e que se limita ao bloco, já a var é uma variável global.

console.log(numeroUm)
//para que serve o console.log e como usá-lo - ele serve para imprimir uma informação

numeroUm=numeroDois;
console.log(numeroUm)

numeroUm=numeroTres;
console.log(numeroUm)